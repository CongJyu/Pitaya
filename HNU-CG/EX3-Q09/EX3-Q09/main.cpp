#include <iostream>
int main() {
    using namespace std;
    char num;
    cin >> num;
    if (num == '0')
        cout << "Zero" << endl;
    else if (num == '1')
        cout << "One" << endl;
    else if (num == '2')
        cout << "Two" << endl;
    else if (num == '3')
        cout << "Three" << endl;
    else if (num == '4')
        cout << "Four" << endl;
    else if (num == '5')
        cout << "Five" << endl;
    else if (num == '6')
        cout << "Six" << endl;
    else if (num == '7')
        cout << "Seven" << endl;
    else if (num == '8')
        cout << "Eight" << endl;
    else if (num == '9')
        cout << "Nine" << endl;
    else
        cout << "Other" << endl;
    return 0;
}
